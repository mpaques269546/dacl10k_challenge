class DWConv(nn.Module):
    def __init__(self, dim=384):
        super().__init__()
        self.dwconv = nn.Conv2d(dim, dim, 3, 1, 1, bias=True, groups=dim)

    def forward(self, x, H, W):
        
        B, N, C = x.shape
        # B: batchsize
        # N: (HW/8**2 + HW/16**2 + HW/32**2)=784+196+49 ->1029
        # C: channels ->96
        n = N // 21 # why 21? 21= N/n = 1029/((224/8)**2/16)

        #if self.patch_size==16 :
        #x1 = x[:, 0:16 * n, :].transpose(1, 2).view(B, C, H * 2, W * 2).contiguous() # (B,HW/8**2,C)
        #x2 = x[:, 16 * n:20 * n, :].transpose(1, 2).view(B, C, H, W).contiguous() # (B,HW/16**2,C)
        #x3 = x[:, 20 * n:, :].transpose(1, 2).view(B, C, H // 2, W // 2).contiguous() # (B,HW/32**2,C)
        # adaptation: if patch_size== 8:
        x1 = x[:, 0:16 * n, :].transpose(1, 2).view(B, C, H , W ).contiguous() # (B,HW/8**2,C)
        x2 = x[:, 16 * n:20 * n, :].transpose(1, 2).view(B, C, H//2, W//2).contiguous() # (B,HW/16**2,C)
        x3 = x[:, 20 * n:, :].transpose(1, 2).view(B, C, H//4, W//4).contiguous() # (B,HW/32**2,C)

        x1 = self.dwconv(x1).flatten(2).transpose(1, 2)
        x2 = self.dwconv(x2).flatten(2).transpose(1, 2)
        x3 = self.dwconv(x3).flatten(2).transpose(1, 2)
        x = torch.cat([x1, x2, x3], dim=1)
        return x

class ConvFFN(nn.Module):
    def __init__(self, in_features, hidden_features=None, out_features=None,
                 act_layer=nn.GELU, drop=0.):
        super().__init__()
        out_features = out_features or in_features
        hidden_features = hidden_features or in_features
        self.fc1 = nn.Linear(in_features, hidden_features)
        self.dwconv = DWConv(hidden_features)
        self.act = act_layer()
        self.fc2 = nn.Linear(hidden_features, out_features)
        self.drop = nn.Dropout(drop)

    def forward(self, x, H, W):
        x = self.fc1(x)
        x = self.dwconv(x, H, W)
        x = self.act(x)
        x = self.drop(x)
        x = self.fc2(x)
        x = self.drop(x)
        return x

